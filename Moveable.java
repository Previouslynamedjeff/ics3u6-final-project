/**
 * THis interface contains the required methods for an object that can be moved.
 */
public interface Moveable {
    /**
     * This method moves the object upwards.
     */
    public void moveUp();

    /**
     * THis method moves the object left.
     */
    public void moveLeft();

    /**
     * This method moves the object down.
     */
    public void moveDown();

    /**
     * This method moves the object right.
     */
    public void moveRight();
}
